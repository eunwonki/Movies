import Foundation

extension String {
    var isNotEmpty: Bool {
        return !isEmpty
    }
}

extension Array {
    var isNotEmpty: Bool {
        return !isEmpty
    }
}

extension Dictionary {
    var isNotEmpty: Bool {
        return !isEmpty
    }
}
